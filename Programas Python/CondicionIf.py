"""
CONDICION IF
Determinar el numero mayor de dos numeros
"""
n1=int(input("Digite un numero: "))
n2=int(input("Digiye otro numero: "))

if(n1>n2):
    print("\nEl primer numero es mayor")
elif(n1==n2):
    print("\nLos numeros son iguales")
else:
    print("\nEl segundo numero es mayor")
    